    coroutineScope {
        val job = listUrls.mapIndexed() { index, url ->
            launch { downloadImage(url, "E:\\ktorJwtAuth\\KotlinCoroutinesFinalTask\\src\\main\\kotlin\\images\\image${index}.jpg") }
        }
        job.joinAll()
    }
}